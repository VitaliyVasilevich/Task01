//Напишите программу, где пользователь вводит любое целое положительное число. А программа 
//суммирует все числа от 1 до введенного пользователем числа.
public class Task13 {

	public static void main(String[] args) {

		int A = 6;
		int i = 1;
		int sum = 0;
		while (i <= A) {
			sum += i;
			i++;
		}
	}

}
